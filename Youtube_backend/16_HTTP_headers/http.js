// HTTP Headers:-
// HTTP headers are key-value pairs sent in HTTP requests and responses.
// They provide essential information about the request or response, such as content type, content length, authentication tokens, and caching directives.
// Common HTTP headers include:
// - `Content-Type`: Indicates the media type of the resource (e.g., application/json).
// - `Authorization`: Contains credentials for authenticating the client with the server.
// - `User-Agent`: Identifies the client software making the request.
// - `Accept`: Specifies the media types that the client is willing to receive in the response.

//*Note:* When we create a custom header, we should follow the naming convention of prefixing the header name with "x-" to indicate that it is a non-standard header.