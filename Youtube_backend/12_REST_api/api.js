// RESTful API:-
// A RESTful API (or REST API) is a way for two systems (like a client and a server) to communicate over the internet using HTTP methods in a standardized way.
// REST stands for *Representational State Transfer* – it’s just a set of rules for building APIs.

// Rules:-
// 1.Server-Client Architecture: 
// REST APIs follow a client-server architecture, where the client and server are separate entities that communicate over HTTP.
// 2.Always respect HTTP methods:-
// REST APIs should use standard HTTP methods (GET, POST, PUT, DELETE) appropriately to perform CRUD operations.
// 3.Statelessness: 
// Each request from the client to the server must contain all the information needed to understand and process the request. The server should not store any client context between requests.
// 4.Cacheability: 
// Responses from the server can be cached by the client or intermediary servers to improve performance and reduce latency.
// 5.Uniform Interface: 
// REST APIs should have a consistent and uniform interface, making it easier for clients to interact with the API.
// 6.Resource-Based: 
// REST APIs are centered around resources, which are identified by unique URIs. Clients interact with these resources using standard HTTP methods.
