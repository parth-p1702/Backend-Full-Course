// HTTP Status code:-
// HTTP status codes are issued by a server in response to a client's request made to the server.
// They represent the outcome of the server's attempt to process the request.
// Status codes are grouped into five categories:
// 1. Informational responses (100–199)
// 2. Successful responses (200–299)
// 3. Redirection messages (300–399)
// 4. Client error responses (400–499)
// 5. Server error responses (500–599)

// Some common HTTP status codes include:
// - 200 OK: The request was successful.
// - 201 Created: The request was successful and a resource was created.
// - 204 No Content: The request was successful but there is no content to send in the response.
// - 400 Bad Request: The server could not understand the request due to invalid syntax.
// - 401 Unauthorized: The client must authenticate itself to get the requested response.
// - 404 Not Found: The server could not find the requested resource.
// - 500 Internal Server Error: The server encountered an unexpected condition.
